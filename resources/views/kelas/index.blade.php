@push('myscript')
    <script>
        $(function(){
            $("#btnTambahkelas").click(function(){
                $("#modal-inputkelas").modal("show");
            });

            $(".edit").click(function(){
                var kode_kls = $(this).attr('kode_kls');
                $.ajax({
                    type: 'POST',
                    url: '/kelas/edit',
                    chace: false,
                    data: {
                        _token: "{{ csrf_token(); }}",
                        kode_kls: kode_kls
                    },
                    success:function(respond){
                        $("#loadeditform").html(respond);
                    }
                });
                $("#modal-editkelas").modal("show");
            });

            $(".konfir_hapus").click(function(e) {
                var form = $(this).closest('form');
                e.preventDefault();
                Swal.fire({
                    title: "Apakah Anda Yakin ?",
                    text: "Ingin Menghapus Data ini ?",
                    icon: "warning",
                    showCancelButton: true,
                    confirmButtonColor: "#d33",
                    confirmButtonText: "Iya, Saya Yakin"
                }).then((result) => {
                    if (result.isConfirmed) {
                        form.submit();
                        Swal.fire({
                        title: "Terhapus",
                        text: "Data Anda Sudah Terhapus",
                        icon: "success"
                        });
                    }
                });
            });

            $("#formkelas").submit(function(){
                var kode_kls = $("formmahasiswa").find("#kode_kls").val();
                var kls = $("#kls").val();
                if (kode_kls== "") {
                    Swal.fire({
                        title: 'Warning!!!',
                        text: 'Kode Kelas Harus Diisi',
                        icon: 'warning',
                        confirmButtonText: 'OK'
                    }).then ((result) => {
                        $("#kode_kls").focus();
                    });
                    return false;
                } else if (kls== "") {
                    Swal.fire({
                        title: 'Warning!!!',
                        text: 'Kelas Harus Diisi',
                        icon: 'warning',
                        confirmButtonText: 'OK'
                    }).then ((result) => {
                        $("#kls").focus();
                    });
                    return false;
                }
            });
        });
    </script>
